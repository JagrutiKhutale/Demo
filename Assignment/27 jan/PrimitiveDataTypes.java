class AddIntAndFloat {
    public static void main(String[] args) {

        int num1 = 10;
        float num2 = 5.5f;

        float sum = num1 + num2;

        System.out.println("Integer value: " + num1);
        System.out.println("Float value  : " + num2);
        System.out.println("Sum           : " + sum);
    }
}
